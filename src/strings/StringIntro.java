package src.strings;

public class StringIntro {
    public static void main(String[] args) {
        // String: reference type
        // provide useful methods
        // index based structure, index starts with 0
        //       index:0123
        String name = "Java";

        // print character located at index 0
        System.out.println(name.charAt(0));

        // print character located at index 1
        System.out.println(name.charAt(1));

        // print character located at index 2
        System.out.println(name.charAt(2));

        // print character located at index 3
        System.out.println(name.charAt(3));

        //indexes need to be in a correct range






    }

}
