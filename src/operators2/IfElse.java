package operators2;

public class IfElse {
    public static void main(String[] args) {

        // a pattern: if (condition){true section}else{false section}
        if(false){
            System.out.println("Hi from if section!");
        }else{
            System.out.println("Hi from else section!");
        }

        System.out.println("The end!");







    }

}
